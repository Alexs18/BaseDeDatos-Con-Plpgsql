SELECT SALDO_CUENTA, COUNT(*) AS CANTIDAD_CEV FROM CUENTA
	WHERE SALDO_CUENTA >=500
	GROUP BY SALDO_CUENTA
/*GROUP BY CON MAS DE UN DATO DE ENTRADA*/
SELECT SALDO_CUENTA, NOMBRE_TITULAR, COUNT(*) AS CANTIDADES FROM CUENTA
	GROUP BY SALDO_CUENTA, NOMBRE_TITULAR

/*SALDO MAYOR EN LA CONSULTA*/
SELECT SALDO_CUENTA, NOMBRE_TITULAR,
	MAX(SALDO_CUENTA) AS SALDO_MAXIMO FROM CUENTA
		GROUP BY SALDO_CUENTA, NOMBRE_TITULAR

/*PODEMOS USAR LA CLAUSULA BETWEEN PARA PODER VER LOS VALORES
DE UN DATO QUE SE ENCUENTRE EN CIERTO RANGO*/
SELECT SALDO_CUENTA, NOMBRE_TITULAR, 
	MIN(SALDO_CUENTA) AS SALDO_MINIMO 
		FROM CUENTA
		WHERE SALDO_CUENTA BETWEEN 500.00 AND 800.00
	GROUP BY 
		SALDO_CUENTA, NOMBRE_TITULAR